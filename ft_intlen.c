#include "libft.h"

size_t		ft_intlen(int n)
{
	size_t	c;

	c = 0;
	if (n == -2147483648)
		return (11);
	if (n <= 0)
	{
		c = 1;
		n *= -1;
	}
	while (n > 0)
	{
		n /= 10;
		c++;
	}
	return (c);
}
