#include "libft.h"

size_t		ft_wrdcnt(const char *s, char c)
{
	size_t	w;

	w = 0;
	while (*s)
	{
		if (*s != c)
			w++;
		while (*s != c && *(s + 1))
			s++;
		s++;
	}
	return (w);
}
